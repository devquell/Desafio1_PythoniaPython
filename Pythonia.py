#Era uma vez, em um reino chamado Pythonia, onde todos os habitantes eram programadores. Nesse reino, as pessoas se comunicavam com o computador usando uma linguagem mágica chamada Python. 
#Um dia, uma criança curiosa chamada Luna decidiu aprender a falar essa linguagem para se tornar uma grande programadora.
#Luna foi até a Montanha do Código, onde morava o sábio Mestre Byte, o maior programador de Pythonia. Ele concordou em ensinar Luna os segredos do Python.

#O Comando print: A Magia de Mostrar Mensagens

#Mestre Byte começou com o comando mais simples: o print. Ele explicou que o print é como uma varinha mágica que faz o computador mostrar mensagens na tela.

print("Olá, mundo do Python!")

#As Variáveis Mágicas

#Mestre Byte então mostrou a Luna algo incrível: as variáveis. Ele explicou que as variáveis são como caixinhas mágicas onde podemos guardar informações. 
#Cada caixinha tem um nome, e podemos colocar dentro dela números, textos ou até mesmo listas de coisas.

nome = "Luna"
idade = 10
altura = 1.35

#Mestre Byte explicou que:

#nome é uma caixinha que guarda o texto "Luna".
#idade é uma caixinha que guarda o número 10.
#altura é uma caixinha que guarda o número 1.35.

#Usando Variáveis com print

#Mestre Byte mostrou como usar as variáveis com o print para mostrar as informações guardadas nelas.

nome = "Luna"
print("Meu nome é " + nome)

#Mestre Byte explicou que o str() transforma números em texto para que possam ser usados em mensagens.

#Removendo Espaços com lstrip(), rstrip() e strip()

#Mestre Byte então explicou que, às vezes, as strings (textos) vêm com espaços extras que atrapalham. Ele ensinou Luna a usar três comandos mágicos para limpar esses espaços:

#lstrip(): Remove espaços à esquerda.
texto = "   Olá, Luna!"
print(texto.lstrip())
#rstrip(): Remove espaços à direita.
texto = "Olá, Luna!   "
print(texto.rstrip())
#strip(): Remove espaços dos dois lados.
texto = "   Olá, Luna!   "
print(texto.strip())

#Luna adorou como os textos ficaram limpos e organizados!

#O Zen do Python com import this
#Mestre Byte então contou a Luna sobre um segredo muito especial: o Zen do Python. Ele disse que, ao digitar import this, o computador revela princípios importantes para escrever um bom código.

import this

#Luna viu mensagens como:

#"Bonito é melhor que feio."
#"Simples é melhor que complexo."
#Ela achou que esses princípios eram como conselhos para a vida, não só para programação.

#Operadores Aritméticos: Fazendo Contas Mágicas

#Por fim, Mestre Byte ensinou Luna a usar os operadores aritméticos para fazer cálculos:

#Adição (+)
soma = 5 + 3
print(soma)

#Subtração (-):
diferenca = 10 - 4
print(diferenca)

#Multiplicação (*):
produto = 2 * 6
print(produto)

#Divisão (/):
divisao = 20 / 5
print(divisao)

#Exponenciação (**):
potencia = 2 ** 3
print(potencia)

#Luna ficou impressionada com o poder desses operadores!
